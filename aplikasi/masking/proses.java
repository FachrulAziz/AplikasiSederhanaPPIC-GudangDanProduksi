    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        if (idTxt.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Proses Terlebih Dahulu Sebelum Disimpan!");
        } else if (meteran.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Nama Belum diisi");
        } else {
        try {
            Connection con = (Connection) koneksi.getConnection();
            String sql = "INSERT INTO tb_proses VALUES ('"+namaMuncul.getText()+"','"+shiftMuncul.getText()+"','"+idTxt.getText()+"','"+warnaTxt.getText()+"','"+ketebalanTxt.getText()+"','"+ ukuranTxt.getText()+"','"+meteran.getText()+"','"+mAtas.getText()+"','"+mBawah.getText()+"','"+idAtasTxt.getText()+"','"+idBawahTxt.getText()+"')";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.execute();
            String sq = "update tb_masking set jenis_masking='"+mAtas.getText()+"',id_masking='"+idAtasTxt.getText()+"',qty_masking='"+panjangMaskingAtas.getText()+"'where id_masking='"+idAtasTxt.getText()+"'";                                    
            PreparedStatement ps = con.prepareStatement(sq);
            ps.execute();
            String s = "update tb_masking set jenis_masking='"+mBawah.getText()+"',id_masking='"+idBawahTxt.getText()+"',qty_masking='"+panjangMaskingBawah.getText()+"'where id_masking='"+idBawahTxt.getText()+"'";                                    
            PreparedStatement p = con.prepareStatement(s);
            p.execute();
            String x = "delete from tb_ppic where id='" + idTxt.getText()+"'";
            PreparedStatement st = con.prepareStatement(x);
            st.execute();
            
            JOptionPane.showMessageDialog(null, "Berhasil Menyimpan");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Gagal Menyimpan" + e.getMessage());
        }
        hapus();
        }
    }//GEN-LAST:event_jButton4MouseClicked

