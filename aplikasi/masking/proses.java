    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(idTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "ID Pembeli Belum diisi");
        } else if (idAtasTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Data Masking Atas Belum Lengkap");
        } else if (idBawahTxt.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Data Masking Bawah Belum Lengkap");
        } else if (meteran.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Isi Counter Meter Akhir");
        } else {
        
       int atas;
       int bawah;
       int totalA;
       int totalB;
       int counter;
        atas = Integer.parseInt(panjangMaskingAtas.getText());
        bawah = Integer.parseInt(panjangMaskingBawah.getText());
        counter = Integer.parseInt(meteran.getText());
        totalA = (atas-counter);
        totalB = (bawah-counter);
        panjangMaskingAtas.setText("" + totalA); 
        panjangMaskingBawah.setText("" + totalB);
        JOptionPane.showMessageDialog(null, "Selesai di Proses");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

